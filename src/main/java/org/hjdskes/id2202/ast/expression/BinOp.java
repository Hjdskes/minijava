package org.hjdskes.id2202.ast.expression;

/**
 * An enumeration for all supported binary operators.
 */
public enum BinOp {
    /**
     * The logical {@code and} operator.
     */
    AND,

    /**
     * The relational {@code <} operator.
     */
    LESSTHAN,

    /**
     * The mathematical {@code *} operator.
     */
    TIMES,

    /**
     * The mathematical {@code +} operator.
     */
    PLUS,

    /**
     * The mathematical {@code -} operator.
     */
    MINUS,
}
